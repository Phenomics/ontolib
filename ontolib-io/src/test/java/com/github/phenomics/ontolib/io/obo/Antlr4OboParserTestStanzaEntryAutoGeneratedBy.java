package com.github.phenomics.ontolib.io.obo;

import static org.junit.Assert.assertEquals;
import static org.junit.Assert.assertNull;

import org.junit.Test;

import com.github.phenomics.ontolib.io.obo.StanzaEntry;
import com.github.phenomics.ontolib.io.obo.StanzaEntryAutoGeneratedBy;
import com.github.phenomics.ontolib.io.obo.StanzaEntryType;

import de.charite.compbio.ontolib.io.obo.parser.Antlr4OboParser;
import de.charite.compbio.ontolib.io.obo.parser.Antlr4OboParser.HeaderKeyValueContext;
import de.charite.compbio.ontolib.io.obo.parser.Antlr4OboParser.KeyValueAutoGeneratedByContext;

public class Antlr4OboParserTestStanzaEntryAutoGeneratedBy extends Antlr4OboParserTestBase {

  @Test
  public void testParseNoModifierNoCommentAsTypedefKeyValue() {
    final String text = "auto-generated-by: ACME.exe\n";
    final Antlr4OboParser parser = buildParser(text);
    final HeaderKeyValueContext ctx = parser.headerKeyValue();
    final StanzaEntry stanzaEntry = (StanzaEntry) getOuterListener().getValue(ctx);

    assertEquals(StanzaEntryType.AUTO_GENERATED_BY, stanzaEntry.getType());
    assertNull(stanzaEntry.getTrailingModifier());
    assertNull(stanzaEntry.getComment());
  }

  @Test
  public void testParseNoModifierNoCommentAsKeyValueAltId() {
    final String text = "auto-generated-by: ACME.exe\n";
    final Antlr4OboParser parser = buildParser(text);
    final KeyValueAutoGeneratedByContext ctx = parser.keyValueAutoGeneratedBy();
    final StanzaEntryAutoGeneratedBy stanzaEntry =
        (StanzaEntryAutoGeneratedBy) getOuterListener().getValue(ctx);

    assertEquals(StanzaEntryType.AUTO_GENERATED_BY, stanzaEntry.getType());
    assertEquals("ACME.exe", stanzaEntry.getGenerator());
    assertNull(stanzaEntry.getTrailingModifier());
    assertNull(stanzaEntry.getComment());
  }

  @Test
  public void testParseModifierNoCommentAsKeyValueAltId() {
    final String text = "auto-generated-by: ACME.exe {key=value}\n";
    final Antlr4OboParser parser = buildParser(text);
    final KeyValueAutoGeneratedByContext ctx = parser.keyValueAutoGeneratedBy();
    final StanzaEntryAutoGeneratedBy stanzaEntry =
        (StanzaEntryAutoGeneratedBy) getOuterListener().getValue(ctx);

    assertEquals(StanzaEntryType.AUTO_GENERATED_BY, stanzaEntry.getType());
    assertEquals("ACME.exe", stanzaEntry.getGenerator());
    assertEquals("TrailingModifier [keyValue=[KeyValue [key=key, value=value]]]",
        stanzaEntry.getTrailingModifier().toString());
    assertNull(stanzaEntry.getComment());
  }

  @Test
  public void testParseNoModifierCommentAsKeyValueAltId() {
    final String text = "auto-generated-by: ACME.exe ! comment\n";
    final Antlr4OboParser parser = buildParser(text);
    final KeyValueAutoGeneratedByContext ctx = parser.keyValueAutoGeneratedBy();
    final StanzaEntryAutoGeneratedBy stanzaEntry =
        (StanzaEntryAutoGeneratedBy) getOuterListener().getValue(ctx);

    assertEquals(StanzaEntryType.AUTO_GENERATED_BY, stanzaEntry.getType());
    assertEquals("ACME.exe", stanzaEntry.getGenerator());
    assertNull(stanzaEntry.getTrailingModifier());
    assertEquals("comment", stanzaEntry.getComment().toString());
  }

  @Test
  public void testParseModifierCommentAsKeyValueAltId() {
    final String text = "auto-generated-by: ACME.exe {key=value} ! comment\n";
    final Antlr4OboParser parser = buildParser(text);
    final KeyValueAutoGeneratedByContext ctx = parser.keyValueAutoGeneratedBy();
    final StanzaEntryAutoGeneratedBy stanzaEntry =
        (StanzaEntryAutoGeneratedBy) getOuterListener().getValue(ctx);

    assertEquals(StanzaEntryType.AUTO_GENERATED_BY, stanzaEntry.getType());
    assertEquals("ACME.exe", stanzaEntry.getGenerator());
    assertEquals("TrailingModifier [keyValue=[KeyValue [key=key, value=value]]]",
        stanzaEntry.getTrailingModifier().toString());
    assertEquals("comment", stanzaEntry.getComment());
  }

}
